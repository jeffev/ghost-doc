function TypeScriptView({ contract }: { contract: ContractDefinition }): JSX.Element {
  const code = contractToTs(contract);
  return (
    <div>
      <div className="flex items-center justify-between mb-2">
        <span className="text-xs text-gray-500">TypeScript interfaces inferred from contract</span>
        <button
          onClick={() => {
            const blob = new Blob([code], { type: "text/plain" });
            const url = URL.createObjectURL(blob);
            const a = document.createElement("a");
            a.href = url;
            a.download = `${contract.functionName}.d.ts`;
            a.click();
            URL.revokeObjectURL(url);
          }}
          className="text-xs px-2 py-1 rounded bg-border text-gray-400 hover:bg-accent/20 hover:text-accent transition-colors"
        >
          ↓ Download .d.ts
        </button>
      </div>
      <CopyableBlock content={code} />
    </div>
  );
}

// ---------------------------------------------------------------------------
// Drift view
// ---------------------------------------------------------------------------

function DriftView({ contract }: { contract: ContractDefinition }): JSX.Element {
  const [drift, setDrift] = useState<DriftResult | null>(null);
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState<string | null>(null);

  const fetchDrift = useCallback(async () => {
    setLoading(true);
    setError(null);
    try {
      const res = await fetch(
        `/contracts/${encodeURIComponent(contract.functionName)}/drift?min_samples=1`,
      );
      if (res.status === 404) {
        setError("No saved contract found. Save the contract first.");
        return;
      }
      if (!res.ok) throw new Error(`HTTP ${res.status}`);
      setDrift((await res.json()) as DriftResult);
    } catch (err) {
      setError((err as Error).message);
    } finally {
      setLoading(false);
    }
  }, [contract.functionName]);

  useEffect(() => {
    void fetchDrift();
  }, [fetchDrift]);

  if (loading) return <div className="text-gray-600 text-xs">Comparing…</div>;
  if (error)
    return (
      <div className="space-y-2">
        <div className="text-gray-500 text-xs">{error}</div>
        <button
          onClick={() => void fetchDrift()}
          className="text-xs px-2 py-1 rounded bg-border text-gray-400 hover:text-accent transition-colors"
        >
          Retry
        </button>
      </div>
    );
  if (!drift) return <div className="text-gray-600 text-xs">No drift data.</div>;

  return (
    <div className="space-y-4">
      <div
        className={`flex items-center gap-2 px-3 py-2 rounded border text-xs font-semibold ${
          drift.isBreaking
            ? "bg-red-950/40 border-red-800 text-red-400"
            : "bg-green-950/40 border-green-800 text-green-400"
        }`}
      >
        {drift.isBreaking ? "⚠ Breaking changes detected" : "✓ No breaking changes"}
        <span className="font-normal opacity-70">saved → current</span>
        <button
          onClick={() => void fetchDrift()}
          className="ml-auto text-xs opacity-60 hover:opacity-100"
        >
          ↻
        </button>
      </div>

      {drift.changes.length === 0 ? (
        <div className="text-gray-500 text-xs">Schemas are identical.</div>
      ) : (
        <div className="space-y-2">
          {drift.changes.map((ch, i) => {
            const isBreakingChange = [
              "type_changed",
              "required_added",
              "field_removed",
              "enum_changed",
            ].includes(ch.kind);
            return (
              <div
                key={i}
                className={`px-3 py-2 rounded border text-xs ${isBreakingChange ? "border-red-900/50 bg-red-950/20" : "border-border bg-canvas"}`}
              >
                <div className="flex items-center gap-2 mb-1">
                  <span className="font-mono text-yellow-400">{ch.path}</span>
                  <span
                    className={`px-1.5 py-0.5 rounded text-xs ${isBreakingChange ? "bg-red-900/50 text-red-400" : "bg-border text-gray-400"}`}
                  >
                    {ch.kind}
                  </span>
                </div>
                <div className="flex gap-4 text-xs">
                  <span className="text-gray-500">
                    before:{" "}
                    <span className="font-mono text-red-300">{JSON.stringify(ch.before)}</span>
                  </span>
                  <span className="text-gray-500">
                    after:{" "}
                    <span className="font-mono text-green-300">{JSON.stringify(ch.after)}</span>
                  </span>
                </div>
              </div>
            );
          })}
        </div>
      )}
    </div>
  );
}

