import { useEffect, useState, useCallback, useRef } from "react";

// ---------------------------------------------------------------------------
// Types
// ---------------------------------------------------------------------------

interface JSONSchema {
  type?: string | string[];
  properties?: Record<string, JSONSchema>;
  required?: string[];
  items?: JSONSchema;
  enum?: unknown[];
  format?: string;
  oneOf?: JSONSchema[];
}

interface ContractDefinition {
  version: "1.0";
  functionName: string;
  generatedAt: string;
  sampleCount: number;
  args: JSONSchema[];
  returns: JSONSchema;
  errors?: JSONSchema[];
}

interface ContractViolationDetail {
  path: string;
  expected: string;
  received: string;
  rule: string;
}

interface ContractViolation {
  functionName: string;
  spanId: string;
  traceId: string;
  timestamp: number;
  violations: ContractViolationDetail[];
}

interface SavedContract {
  name: string;
  file: string;
}

interface SchemaChange {
  path: string;
  kind: string;
  before: unknown;
  after: unknown;
}

interface DriftResult {
  functionName: string;
  isBreaking: boolean;
  changes: SchemaChange[];
  saved: ContractDefinition;
  current: ContractDefinition;
}

// ---------------------------------------------------------------------------
// localStorage helpers
// ---------------------------------------------------------------------------

function getPinned(): Set<string> {
  try {
    return new Set(JSON.parse(localStorage.getItem("gd:pinned") ?? "[]") as string[]);
  } catch {
    return new Set();
  }
}

function setPinned(set: Set<string>): void {
  localStorage.setItem("gd:pinned", JSON.stringify([...set]));
}

function getAnnotation(fnName: string): string {
  return localStorage.getItem(`gd:annotation:${fnName}`) ?? "";
}

function setAnnotation(fnName: string, text: string): void {
  if (text.trim() === "") {
    localStorage.removeItem(`gd:annotation:${fnName}`);
  } else {
    localStorage.setItem(`gd:annotation:${fnName}`, text);
  }
}

// ---------------------------------------------------------------------------
// Confidence score (0–100)
// ---------------------------------------------------------------------------

function computeConfidence(contract: ContractDefinition): number {
  const sampleScore = Math.min(contract.sampleCount / 50, 1) * 50;

  function countOneOf(schema: JSONSchema): number {
    let n = schema.oneOf ? 1 : 0;
    if (schema.properties) for (const v of Object.values(schema.properties)) n += countOneOf(v);
    if (schema.items) n += countOneOf(schema.items);
    return n;
  }
  const allSchemas = [...contract.args, contract.returns];
  const oneOfCount = allSchemas.reduce((s, sc) => s + countOneOf(sc), 0);
  const typeScore = Math.max(0, 30 - oneOfCount * 5);

  function hasFormat(schema: JSONSchema): boolean {
    if (schema.format) return true;
    if (schema.properties) return Object.values(schema.properties).some(hasFormat);
    return false;
  }
  const formatBonus = allSchemas.some(hasFormat) ? 20 : 0;

  return Math.round(Math.min(100, sampleScore + typeScore + formatBonus));
}

function confidenceColor(score: number): string {
  if (score >= 80) return "text-green-400";
  if (score >= 50) return "text-yellow-400";
  return "text-orange-400";
}

// ---------------------------------------------------------------------------
// TypeScript interface generator
// ---------------------------------------------------------------------------

function schemaToTs(schema: JSONSchema, indent = 0): string {
  const pad = "  ".repeat(indent);
  if (schema.enum !== undefined) return schema.enum.map((v) => JSON.stringify(v)).join(" | ");
  if (schema.oneOf !== undefined) return schema.oneOf.map((s) => schemaToTs(s, indent)).join(" | ");
  const t = Array.isArray(schema.type) ? schema.type[0] : schema.type;
  switch (t) {
    case "string":
      return schema.format ? `string /* ${schema.format} */` : "string";
    case "number":
      return "number";
    case "boolean":
      return "boolean";
    case "null":
      return "null";
    case "array":
      return `${schema.items ? schemaToTs(schema.items, indent) : "unknown"}[]`;
    case "object": {
      if (!schema.properties) return "Record<string, unknown>";
      const lines = ["{"];
      for (const [key, val] of Object.entries(schema.properties)) {
        const req = schema.required?.includes(key) ? "" : "?";
        lines.push(`${pad}  ${key}${req}: ${schemaToTs(val, indent + 1)};`);
      }
      lines.push(`${pad}}`);
      return lines.join("\n");
    }
    default:
      return "unknown";
  }
}

function contractToTs(contract: ContractDefinition): string {
  const lines = [
    `// auto-generated by Ghost Doc — ${contract.functionName}`,
    `// sampleCount: ${contract.sampleCount}  generatedAt: ${contract.generatedAt}`,
    "",
  ];
  contract.args.forEach((schema, i) => {
    lines.push(`export type ${contract.functionName}Arg${i} = ${schemaToTs(schema)};`);
  });
  if (contract.args.length > 0) lines.push("");
  lines.push(`export type ${contract.functionName}Return = ${schemaToTs(contract.returns)};`);
  if (contract.errors?.length) {
    lines.push("");
    contract.errors.forEach((schema, i) => {
      lines.push(`export type ${contract.functionName}Error${i} = ${schemaToTs(schema)};`);
    });
  }
  lines.push("");
  const argTypes = contract.args.map((_, i) => `${contract.functionName}Arg${i}`).join(", ");
  lines.push(
    `export type ${contract.functionName}Fn = (${argTypes ? `...args: [${argTypes}]` : ""}) => Promise<${contract.functionName}Return>;`,
  );
  return lines.join("\n");
}

