function SchemaView({ contract }: { contract: ContractDefinition }): JSX.Element {
  return (
    <div className="space-y-4">
      {contract.args.length > 0 && (
        <section>
          <h3 className="text-xs font-semibold text-gray-500 uppercase tracking-wider mb-2">
            Arguments
          </h3>
          {contract.args.map((schema, i) => (
            <div key={i} className="mb-2">
              <div className="text-xs text-gray-500 mb-1">arg[{i}]</div>
              <CopyableBlock content={JSON.stringify(schema, null, 2)} />
            </div>
          ))}
        </section>
      )}
      <section>
        <h3 className="text-xs font-semibold text-gray-500 uppercase tracking-wider mb-2">
          Return Value
        </h3>
        <CopyableBlock content={JSON.stringify(contract.returns, null, 2)} />
      </section>
      {contract.errors?.length ? (
        <section>
          <h3 className="text-xs font-semibold text-gray-500 uppercase tracking-wider mb-2">
            Error Shapes
          </h3>
          {contract.errors.map((schema, i) => (
            <CopyableBlock key={i} content={JSON.stringify(schema, null, 2)} />
          ))}
        </section>
      ) : null}
    </div>
  );
}

// ---------------------------------------------------------------------------
// TypeScript view
