function ViolationsFeed({
  violations,
  onClear,
}: {
  violations: ContractViolation[];
  onClear: () => void;
}): JSX.Element {
  if (violations.length === 0) {
    return (
      <div className="text-gray-600 text-xs">
        No violations. Click "Validate spans" to check recent calls against this contract.
      </div>
    );
  }
  return (
    <div>
      <div className="flex items-center justify-between mb-3">
        <span className="text-xs text-gray-500">{violations.length} violation(s)</span>
        <button
          onClick={onClear}
          className="text-xs text-gray-600 hover:text-gray-400 transition-colors"
        >
          Clear
        </button>
      </div>
      <div className="space-y-3">
        {violations.map((v, i) => (
          <div key={i} className="bg-canvas border border-red-900/50 rounded p-3 text-xs">
            <div className="font-mono text-gray-400 mb-2">
              span <span className="text-gray-600">{v.spanId.slice(0, 8)}…</span>
            </div>
            {v.violations.map((d, j) => (
              <div key={j} className="flex gap-2 text-xs mb-1">
                <span className="font-mono text-yellow-400">{d.path}</span>
                <span className="text-gray-600">[{d.rule}]</span>
                <span className="text-gray-500">expected</span>
                <span className="text-green-400">{d.expected}</span>
                <span className="text-gray-500">got</span>
                <span className="text-red-400">{d.received}</span>
              </div>
            ))}
          </div>
        ))}
      </div>
    </div>
  );
}

// ---------------------------------------------------------------------------
// Copyable code block
// ---------------------------------------------------------------------------

function CopyableBlock({ content }: { content: string }): JSX.Element {
  const [copied, setCopied] = useState(false);
  const timer = useRef<ReturnType<typeof setTimeout> | null>(null);
  const handleCopy = useCallback(() => {
    void navigator.clipboard.writeText(content).then(() => {
      setCopied(true);
      if (timer.current !== null) clearTimeout(timer.current);
      timer.current = setTimeout(() => setCopied(false), 1500);
    });
  }, [content]);
  return (
    <div className="relative group">
      <pre className="text-xs font-mono bg-canvas border border-border rounded p-3 overflow-x-auto text-gray-300 whitespace-pre-wrap">
        {content}
      </pre>
      <button
        onClick={handleCopy}
        className="absolute top-2 right-2 text-xs px-1.5 py-0.5 rounded bg-border/80 text-gray-500 hover:text-accent hover:bg-accent/20 transition-colors opacity-0 group-hover:opacity-100"
        title="Copy to clipboard"
      >
        {copied ? "✓" : "copy"}
      </button>
    </div>
  );
}

