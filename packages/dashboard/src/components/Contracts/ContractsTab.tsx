type PanelView = "live" | "saved";
type DetailSection = "schema" | "typescript" | "violations" | "drift" | "annotations";

export function ContractsTab(): JSX.Element {
  const [panelView, setPanelView] = useState<PanelView>("live");
  const [contracts, setContracts] = useState<ContractDefinition[]>([]);
  const [savedContracts, setSavedContracts] = useState<SavedContract[]>([]);
  const [selected, setSelected] = useState<ContractDefinition | null>(null);
  const [violations, setViolations] = useState<ContractViolation[]>([]);
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState<string | null>(null);
  const [generating, setGenerating] = useState<string | null>(null);
  const [saving, setSaving] = useState<string | null>(null);
  const [minSamples, setMinSamples] = useState(1);
  const [coverageThreshold, setCoverageThreshold] = useState(10);
  const [pinned, setPinnedState] = useState<Set<string>>(getPinned);
  const [activeSection, setActiveSection] = useState<DetailSection>("schema");

  const fetchContracts = useCallback(async () => {
    setLoading(true);
    setError(null);
    try {
      const res = await fetch(`/contracts?min_samples=${minSamples}`);
      if (!res.ok) throw new Error(`HTTP ${res.status}`);
      setContracts((await res.json()) as ContractDefinition[]);
    } catch (err) {
      setError((err as Error).message);
    } finally {
      setLoading(false);
    }
  }, [minSamples]);

  const fetchSaved = useCallback(async () => {
    try {
      const res = await fetch("/contracts/saved");
      if (!res.ok) return;
      setSavedContracts((await res.json()) as SavedContract[]);
    } catch {
      /* ignore */
    }
  }, []);

  useEffect(() => {
    void fetchContracts();
    void fetchSaved();
  }, [fetchContracts, fetchSaved]);

  const handleGenerateContract = useCallback(
    async (fnName: string) => {
      setGenerating(fnName);
      try {
        const res = await fetch(
          `/contracts/${encodeURIComponent(fnName)}?min_samples=${minSamples}`,
        );
        if (!res.ok) throw new Error(`HTTP ${res.status}`);
        const contract = (await res.json()) as ContractDefinition;
        setContracts((prev) =>
          [...prev.filter((c) => c.functionName !== fnName), contract].sort((a, b) =>
            a.functionName.localeCompare(b.functionName),
          ),
        );
        setSelected(contract);
      } catch (err) {
        setError((err as Error).message);
      } finally {
        setGenerating(null);
      }
    },
    [minSamples],
  );

  const handleValidate = useCallback(async (contract: ContractDefinition) => {
    try {
      const res = await fetch("/contracts/validate", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ contract }),
      });
      if (!res.ok) throw new Error(`HTTP ${res.status}`);
      const data = (await res.json()) as { violations: ContractViolation[]; count: number };
      setViolations(data.violations);
      setActiveSection("violations");
    } catch (err) {
      setError((err as Error).message);
    }
  }, []);

  const handleSave = useCallback(
    async (contract: ContractDefinition) => {
      setSaving(contract.functionName);
      try {
        const res = await fetch("/contracts/save", {
          method: "POST",
          headers: { "Content-Type": "application/json" },
          body: JSON.stringify({ contract }),
        });
        if (!res.ok) throw new Error(`HTTP ${res.status}`);
        await fetchSaved();
      } catch (err) {
        setError((err as Error).message);
      } finally {
        setSaving(null);
      }
    },
    [fetchSaved],
  );

  const handleLoadSaved = useCallback(async (name: string) => {
    try {
      const res = await fetch(`/contracts/saved/${encodeURIComponent(name)}`);
      if (!res.ok) throw new Error(`HTTP ${res.status}`);
      setSelected((await res.json()) as ContractDefinition);
      setViolations([]);
      setActiveSection("schema");
    } catch (err) {
      setError((err as Error).message);
    }
  }, []);

  const togglePin = useCallback((fnName: string) => {
    setPinnedState((prev) => {
      const next = new Set(prev);
      if (next.has(fnName)) next.delete(fnName);
      else next.add(fnName);
      setPinned(next);
      return next;
    });
  }, []);

  return (
    <div className="flex h-full overflow-hidden text-sm">
      {/* Left sidebar */}
      <div className="w-72 flex-shrink-0 border-r border-border flex flex-col overflow-hidden">
        {/* Controls */}
        <div className="px-3 py-2 border-b border-border flex-shrink-0 space-y-2">
          <div>
            <div className="flex items-center justify-between mb-1">
              <span className="text-xs text-gray-500">Min samples</span>
              <span className="text-xs font-mono text-accent">{minSamples}</span>
            </div>
            <input
              type="range"
              min={1}
              max={50}
              value={minSamples}
              onChange={(e) => setMinSamples(Number(e.target.value))}
              onMouseUp={() => void fetchContracts()}
              onKeyUp={() => void fetchContracts()}
              className="w-full accent-accent"
            />
          </div>
          <div>
            <div className="flex items-center justify-between mb-1">
              <span className="text-xs text-gray-500">Coverage gate</span>
              <span className="text-xs font-mono text-yellow-400">{coverageThreshold}</span>
            </div>
            <input
              type="range"
              min={1}
              max={100}
              value={coverageThreshold}
              onChange={(e) => setCoverageThreshold(Number(e.target.value))}
              className="w-full accent-yellow-400"
            />
          </div>
        </div>

        {/* View toggle */}
        <div className="flex border-b border-border flex-shrink-0">
          <PanelTab
            active={panelView === "live"}
            onClick={() => {
              setPanelView("live");
              void fetchContracts();
            }}
          >
            Live ({contracts.length})
          </PanelTab>
          <PanelTab
            active={panelView === "saved"}
            onClick={() => {
              setPanelView("saved");
              void fetchSaved();
            }}
          >
            Saved ({savedContracts.length})
          </PanelTab>
        </div>

        <div className="flex-1 overflow-y-auto">
          {loading && <div className="px-3 py-4 text-gray-600 text-xs">Loading…</div>}
          {error !== null && <div className="px-3 py-2 text-red-400 text-xs">{error}</div>}

          {panelView === "live" && !loading && contracts.length === 0 && (
            <div className="px-3 py-4 text-gray-600 text-xs">
              No contracts yet. Need ≥ {minSamples} call{minSamples !== 1 ? "s" : ""} per function.
            </div>
          )}

          {panelView === "live" &&
            contracts
              .slice()
              .sort((a, b) => a.functionName.localeCompare(b.functionName))
              .map((c) => {
                const conf = computeConfidence(c);
                const belowGate = c.sampleCount < coverageThreshold;
                const isPinned = pinned.has(c.functionName);
                return (
                  <button
                    key={c.functionName}
                    onClick={() => {
                      setSelected(c);
                      setViolations([]);
                      setActiveSection("schema");
                    }}
                    className={`w-full text-left px-3 py-2 border-b border-border/50 transition-colors ${
                      selected?.functionName === c.functionName
                        ? "bg-accent/20 text-accent"
                        : "text-gray-300 hover:bg-canvas/50"
                    } ${belowGate ? "border-l-2 border-l-yellow-600" : ""}`}
                  >
                    <div className="flex items-center justify-between gap-1">
                      <span className="font-mono truncate text-xs">{c.functionName}</span>
                      <div className="flex items-center gap-1 flex-shrink-0">
                        {isPinned && <span title="Pinned">📌</span>}
                        {belowGate && (
                          <span
                            className="text-yellow-400 text-xs"
                            title={`Only ${c.sampleCount} samples (gate: ${coverageThreshold})`}
                          >
                            ⚠
                          </span>
                        )}
                        <span className={`text-xs font-mono ${confidenceColor(conf)}`}>
                          {conf}%
                        </span>
                      </div>
                    </div>
                    <div className="text-xs text-gray-600 mt-0.5">
                      {c.sampleCount} samples · {c.args.length} arg{c.args.length !== 1 ? "s" : ""}
                      {c.errors?.length ? (
                        <span className="ml-1 text-orange-400">· errors</span>
                      ) : null}
                    </div>
                  </button>
                );
              })}

          {panelView === "saved" && savedContracts.length === 0 && (
            <div className="px-3 py-4 text-gray-600 text-xs">No saved contracts.</div>
          )}
          {panelView === "saved" &&
            savedContracts.map((s) => (
              <button
                key={s.name}
                onClick={() => void handleLoadSaved(s.name)}
                className="w-full text-left px-3 py-2 border-b border-border/50 text-gray-300 hover:bg-canvas/50 transition-colors"
              >
                <div className="font-mono text-xs truncate">{s.name}</div>
              </button>
            ))}
        </div>
      </div>

      {/* Right: detail panel */}
      {selected === null ? (
        <div className="flex-1 flex items-center justify-center text-gray-600 text-sm">
          Select a function to view its contract
        </div>
      ) : (
        <ContractDetail
          contract={selected}
          violations={violations}
          generating={generating}
          saving={saving}
          isPinned={pinned.has(selected.functionName)}
          activeSection={activeSection}
          onSectionChange={setActiveSection}
          onGenerate={() => void handleGenerateContract(selected.functionName)}
          onValidate={() => void handleValidate(selected)}
          onSave={() => void handleSave(selected)}
          onTogglePin={() => togglePin(selected.functionName)}
          onClearViolations={() => setViolations([])}
        />
      )}
    </div>
  );
}

// ---------------------------------------------------------------------------
// Contract detail panel
// ---------------------------------------------------------------------------

function ContractDetail({
  contract,
  violations,
  generating,
  saving,
  isPinned,
  activeSection,
  onSectionChange,
  onGenerate,
  onValidate,
  onSave,
  onTogglePin,
  onClearViolations,
}: {
  contract: ContractDefinition;
  violations: ContractViolation[];
  generating: string | null;
  saving: string | null;
  isPinned: boolean;
  activeSection: DetailSection;
  onSectionChange: (s: DetailSection) => void;
  onGenerate: () => void;
  onValidate: () => void;
  onSave: () => void;
  onTogglePin: () => void;
  onClearViolations: () => void;
}): JSX.Element {
  const conf = computeConfidence(contract);

  return (
    <div className="flex-1 flex flex-col overflow-hidden">
      {/* Header */}
      <div className="px-4 py-3 border-b border-border flex items-center gap-2 flex-shrink-0 flex-wrap">
        <span className="font-mono font-semibold text-accent">{contract.functionName}</span>
        {isPinned && (
          <span className="text-xs text-yellow-400" title="Pinned — Re-infer won't overwrite">
            📌 pinned
          </span>
        )}
        <span className="text-xs text-gray-600">
          {contract.sampleCount} samples · {new Date(contract.generatedAt).toLocaleString()}
        </span>
        <span
          className={`text-xs font-mono font-semibold ${confidenceColor(conf)}`}
          title="Confidence score"
        >
          {conf}% confidence
        </span>
        <div className="flex-1" />
        <button
          onClick={onTogglePin}
          className={`text-xs px-2 py-1 rounded border transition-colors ${isPinned ? "border-yellow-700 text-yellow-400 bg-yellow-900/20" : "border-border text-gray-500 hover:border-yellow-700 hover:text-yellow-400"}`}
          title={isPinned ? "Unpin contract" : "Pin contract (protects from re-inference)"}
        >
          {isPinned ? "Unpin" : "Pin"}
        </button>
        <button
          onClick={isPinned ? undefined : onGenerate}
          disabled={generating === contract.functionName || isPinned}
          className="text-xs px-2 py-1 rounded bg-accent/20 text-accent hover:bg-accent/30 transition-colors disabled:opacity-50"
          title={isPinned ? "Unpin to re-infer" : "Re-infer contract from current spans"}
        >
          {generating === contract.functionName ? "Inferring…" : "↻ Re-infer"}
        </button>
        <button
          onClick={onValidate}
          className="text-xs px-2 py-1 rounded bg-border text-gray-400 hover:bg-accent/20 hover:text-accent transition-colors"
        >
          Validate spans
        </button>
        <button
          onClick={onSave}
          disabled={saving === contract.functionName}
          className="text-xs px-2 py-1 rounded bg-border text-gray-400 hover:bg-green-900/40 hover:text-green-400 transition-colors disabled:opacity-50"
          title="Save contract to disk"
        >
          {saving === contract.functionName ? "Saving…" : "↓ Save"}
        </button>
      </div>

      {/* Section tabs */}
      <div className="flex border-b border-border flex-shrink-0 overflow-x-auto">
        {(["schema", "typescript", "violations", "drift", "annotations"] as DetailSection[]).map(
          (sec) => (
            <SectionTab
              key={sec}
              active={activeSection === sec}
              onClick={() => onSectionChange(sec)}
            >
              {sec === "violations" ? (
                <>
                  Violations
                  {violations.length > 0 && (
                    <span className="ml-1.5 bg-red-700 text-white text-xs rounded-full px-1.5">
                      {violations.length}
                    </span>
                  )}
                </>
              ) : (
                sec.charAt(0).toUpperCase() + sec.slice(1)
              )}
            </SectionTab>
          ),
        )}
      </div>

      <div className="flex-1 overflow-y-auto p-4">
        {activeSection === "schema" && <SchemaView contract={contract} />}
        {activeSection === "typescript" && <TypeScriptView contract={contract} />}
        {activeSection === "violations" && (
          <ViolationsFeed violations={violations} onClear={onClearViolations} />
        )}
        {activeSection === "drift" && <DriftView contract={contract} />}
        {activeSection === "annotations" && <AnnotationsView fnName={contract.functionName} />}
      </div>
    </div>
  );
}

